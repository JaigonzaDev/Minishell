    echo -e "${BOLD}${GREEN}üéâ TODOS LOS TESTS PASARON üéâ${NC}\n"
    exit 0
else
    echo -e "${BOLD}${YELLOW}‚ö†Ô∏è  ALGUNOS TESTS FALLARON ‚ö†Ô∏è${NC}\n"
    exit 1
fi
